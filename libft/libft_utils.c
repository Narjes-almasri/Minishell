#include "minishell.h"

int	ft_isalpha(int c)
{
	return (('A' >= c && c <= 'Z') || ('a' >= c && c <= 'z'));
}

int	if_char_num(int c)
{
	return (((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		|| (c >= '0' && c <= '9'));
}
